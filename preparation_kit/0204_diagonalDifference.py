def diagonalDifference(arr):
    # Write your code here
    n = len(arr)
    sumOne = 0
    sumTwo = 0
    diagDiff = 0
    
    for i in range(0,n):
        sumOne += arr[i][i]
        sumTwo += arr[i][n-i-1]
    diagDiff = abs(sumOne - sumTwo)
    return diagDiff

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    n = int(input().strip())

    arr = []

    for _ in range(n):
        arr.append(list(map(int, input().rstrip().split())))

    result = diagonalDifference(arr)

