def twoArrays(k, A, B):
    # Write your code here
    
    A.sort()
    B.sort(reverse=True)

    notValid = []
    for i in range(len(A)):
        if A[i] + B[i] < k:
            notValid.append(True)   
        else:
            notValid.append(False)

    if any(notValid):
        valid = 'NO'
    else:
        valid = 'YES'     
    return valid

